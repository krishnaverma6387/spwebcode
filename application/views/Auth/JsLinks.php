	function Status(e, table, where_column, where_value, column) {
		var status = true;
		var check = $(e).prop("checked");
		if (check) {
			swal({
				title: "Are you sure?",
				text: "You want to activate this !",
				icon: "warning",
				buttons: true,
				dangerMode: true
			}).then((willDelete) => {
				if (willDelete) {
					$.ajax({
						url: "<?php echo base_url("Auth/UpdateStatus"); ?>",
						type: "post",
						data: {
							'table': table,
							'column': column,
							'value': 'true',
							'where_column': where_column,
							'where_value': where_value
						},
						success: function(response) {
							swal("Activated successfully !", {
								icon: "success",
							});
						}
					});
				} else {
					window.setTimeout(function() {
						location.reload();
					}, 1000);
				}
			});
		} else {
			swal({
				title: "Are you sure?",
				text: "You want to deactivate this !",
				icon: "warning",
				buttons: true,
				dangerMode: true
			}).then((willDelete) => {
				if (willDelete) {
					$.ajax({
						url: "<?php echo base_url("Auth/UpdateStatus"); ?>",
						type: "post",
						data: {
							'table': table,
							'column': column,
							'value': 'false',
							'where_column': where_column,
							'where_value': where_value
						},
						success: function(response) {
							swal("Deactivated successfully !", {
								icon: "success",
							});
						}
					});
				} else {
					window.setTimeout(function() {
						location.reload();
					}, 1000);
				}
			});
		}
		return status;
	}

